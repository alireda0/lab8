    private void loadQuestionData() {
        if (lesson.getQuiz() == null) return;
        Question q = lesson.getQuiz().getQuestions().get(editIndex);
        
        txtQuestion.setText(q.getQuestionText());
        
        List<String> ops = q.getOptions();
        if (ops.size() > 0) txtOp1.setText(ops.get(0));
        if (ops.size() > 1) txtOp2.setText(ops.get(1));
        if (ops.size() > 2) txtOp3.setText(ops.get(2));
        if (ops.size() > 3) txtOp4.setText(ops.get(3));
        
        // +1 because array is 0-based, but user types 1-4
        txtCorrect.setText(String.valueOf(q.getCorrectOptionIndex() + 1)); 
    }

