    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed

    String qText = txtQuestion.getText().trim();
    String o1 = txtOp1.getText().trim();
    String o2 = txtOp2.getText().trim();
    String o3 = txtOp3.getText().trim();
    String o4 = txtOp4.getText().trim();
    String correctStr = txtCorrect.getText().trim();

    // 1. Validation: Question + at least 2 options
    if (qText.isEmpty() || o1.isEmpty() || o2.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Please enter the Question and at least Option 1 & Option 2.");
        return;
    }

    // 2. Build options list
    List<String> options = new ArrayList<>();
    options.add(o1);
    options.add(o2);
    if (!o3.isEmpty()) options.add(o3);
    if (!o4.isEmpty()) options.add(o4);

    // 3. Validate correct answer index
    int correctIndex;
    try {
        int choice = Integer.parseInt(correctStr);
        if (choice < 1 || choice > options.size()) {
            JOptionPane.showMessageDialog(this,
                    "Correct choice must be a number between 1 and " + options.size());
            return;
        }
        correctIndex = choice - 1; // 0-based
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Correct choice must be a valid number (e.g., 1).");
        return;
    }

    // 4. Ensure quiz exists
    if (lesson.getQuiz() == null) {
        lesson.setQuiz(new Quiz());
    }

    Question newQ = new Question(qText, options, correctIndex);

    // 5. ADD vs EDIT (no extra add!)
    if (editIndex == -1) {
        // ADD MODE
        lesson.getQuiz().getQuestions().add(newQ);
        JOptionPane.showMessageDialog(this, "Question added successfully!");
    } else {
        // EDIT MODE
        lesson.getQuiz().getQuestions().set(editIndex, newQ);
        JOptionPane.showMessageDialog(this, "Question updated successfully!");
    }

    // 6. Save and close
    JsonDatabaseManager db = new JsonDatabaseManager();
    db.updateCourse(course);
    this.dispose();
    }//GEN-LAST:event_AddActionPerformed

